            <h4>What kinds of visas are available?</h4>
            <p>
                <b> Employment visa </b>– 15 to 20 working days<br/>
                <b>Visit visas</b> – either on arrival or 7 working days
            </p>

            <h4>What is an employment visa?</h4>
            <p>The employment visa is the official work authorization for the foreign national. The employee is sponsored by the UAE company.  </p>

            <h4>What is a business visit visa?</h4>
            <p>
                It is generally a short-term single entry visit visa to conduct business activities in the destination country. The activities are limited to
                <br/><br/>
                • Attending business meetings or discussions<br/>
                • Contact potential UAE clients<br/>
                • Attending seminars<br/>
            </p>


            <h4>What is a tourist visa?</h4>
            <p>The tourist visa is generally given for 30 days and allows the tourist to visit the UAE. </p>

            <h4>What is the visa validity?</h4>
            <br/>
            <p><b>Employment visas:</b> In Free Zones 3 years, for onshore companies 2 years, each renewable for the same period<br/>
                <b>Business visit visas:</b> 30 or 90 days – not renewable<br/>
                <b> Tourist visas:</b> 30 days<br/></p>

            <h4>Can an employee sponsor his family members?</h4>
            <br/>
            <p>Once the employee has an employment visa, he can sponsor his family and domestic workers.</p>

