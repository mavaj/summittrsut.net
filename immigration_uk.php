            <br>
            <div><img src="http://www.capitalimmigration.net/Images/uk_012.jpg" alt="europe_002"></div>
            <p><strong>Claiming Points</strong><br>
            <br>
            <strong>Attributes</strong><br>
            <strong>Access to not less than £200,000 [25 points available]</strong><br>
            If you have not less than £200,000 of your own money available to make a fresh investment into business in the United Kingdom you need to supply documentary evidence as specified below. You may include funds made available by one or more other people (known as a third party or parties). In these circumstances you must also provide a declaration from every contributor that the funds are available to you, together with confirmation from a legal representative that the declaration document is valid. If you are relying on money held by a husband, wife or partner that person will be regarded as a third party.<br>
            <br>
            <strong>Funds held in one or more regulated financial institutions [25 points available]</strong><br>
            The financial institution or institutions that provide confirmation of the funds available to you must be regulated by the home regulator. The home regulator is an official financial regulatory body in the country where the financial institution operates and the transaction was made. This body must be appropriate to the type of financial transaction.<br>
            <br>
            <strong>Funds disposable in the United Kingdom [25 points available]</strong><br>
            Funds held in the United Kingdom must be held in an institution that is regulated by the Financial Services Authority (FSA). If the funds are not held in the United Kingdom, all of the £200,000 must be freely transferable to the United Kingdom and convertible into pounds sterling. If the funds are held overseas but in an institution that has a presence in the United Kingdom and is regulated by the FSA, then the institution already does business in the United Kingdom and we will not need any further evidence of the availability of the funds for transfer to the United Kingdom.<br>
            If you rely on funds that are held in an overseas institution that is not regulated by the FSA you must provide confirmation that the funds can be transferred into the United Kingdom. Do this by supplying a document from your bank or financial institution confirming the amount of funds.<br>
            <br>
            <strong>Controls</strong><br>
            <strong>English language ability [10 points available]</strong><br>
            The ability to meet the required English standard is a mandatory requirement. If you cannot meet this level, your application will be refused regardless of any points you may have scored in the Attributes section. 10 points are awarded in this section if you meet the required standard and 0 if you do not.<br>
            There are four ways in which you can meet this requirement. You can:<br>
            be a national of a majority English speaking country on the following list – Antigua and Barbuda, Australia, The Bahamas, Barbados, Belize, Canada, Dominica, Grenada, Guyana, Jamaica, New Zealand, St Christopher (Kitts) and Nevis, St Lucia, St Vincent and the Grenadines, Trinidad and Tobago, the United States of America or hold an English language qualification equivalent to the Council of Europe&rsquo;s Common European Framework for Language Learning level C1 (approximately IELTS level 6.5, GCSE Grade C) or hold a degree that was taught in English as confirmed by the UK NARIC database as equivalent to a UK Bachelors degree or have previously been granted leave under the Business Person Category.<br>
            <br>
            <strong>Maintenance (Funds) [10 points available]</strong><br>
            You must be able to support yourself for the entire duration of your stay in the UK without needing any help from public funds. In order to qualify under Tier 1 (Entrepreneur) you must be able to demonstrate that you have £3,100 for yourself and (where applicable) £1,800 for each dependant accompanying you and that you have held these amounts for a minimum period of 3 months immediately preceding and dated no more than 1 month prior to the date you submit your application. The balance should not fall below the required minimum at any time during the 3 month period.</p>
