            <h4>UK Entrepreneur Category (Business Setup)</h4>
            <p><strong>Tier 1 (UK Entrepreneur Category)</strong><br>
                <br>
                <strong>Important Notice:</strong><br>
                Foreign entrepreneurs and investors are being given an extra incentive to come to the UK today by new visa rules, which will reward those who contribute to economic growth.<br>
                Under changes to the Immigration Rules, which were laid in Parliament today, people who come to the UK under Tier 1 (Investor) of the points-based system will be able to settle here faster if they invest large sums of money.<br>
                Those who invest £5 million will be allowed to settle here after 3 years, and those investing £10 million or more will be allowed to settle after 2 years. This compares with the current minimum 5-year requirement.<br>
                The right to accelerated settlement will apply to those investors who are already in the UK and have invested large sums, as well as new arrivals.<br>
                Tier 1 (Entrepreneur) migrants will also be able to settle in the UK more quickly if they create 10 jobs or turn over £5 million in a 3-year period.<br>
                Tier 1 (General) Migrant of the Points Based System was launched for foreign nationals living in the UK on 29 February 2008. The four sub-tiers are Tier 1 (General) Migrant, Tier 1 (Investor), Tier 1 (Entrepreneur) and Tier 1 (Post-Study Work).<br>
                <br>
                <strong>Benefits of settling in the UK</strong><br>
                United Kingdom is a country with a rich heritage, beautiful scenery and historic London, its capital, is one of the world&rsquo;s recognized financial centers. Modern cosmopolitan cities are interspersed with areas of outstanding natural beauty and picturesque, traditional villages.<br>
                <br>
                <strong>Reasons to choose the United Kingdom:</strong><br>
                One of the highest standards of living in the world and a member of the G-8 group of countries.<br>
                <br>
                A diverse and multicultural society, containing virtually every cultural and ethnic community.<br>
                <br>
                A high standard of education with a broad network of state and private schools, and more than 150 universities and higher education institutions to choose from.<br>
                <br>
                One of the best free medical care services in the world.<br>
                <br>
                A great place to do business. As a member of the European Union, the UK has easy access to the whole of Europe. At its heart, London is unrivalled as a financial business center.<br>
                <br>
                An attractive tax regime for high net worth individuals classed as &ldquo;UK resident, non-domicile&rdquo;<br>
                <br>
                The travel mobility and security of the UK passport.<br>
                <br>
                Points are awarded for what are called &ldquo;Attributes&rdquo;, for which you need to score 75 points. These are awarded as follows:<br>
            </p>
            <ul>
                <li>You have access to not less than £200,000 [25 points available];</li>
                <li>The money is held in one or more regulated financial institutions [25 points available];</li>
                <li>The money is disposable in the United Kingdom [25 points available].</li>
            </ul>
            <br>
            <br>
            Separate points are given for English language ability (10 points) and the availability of funds for maintenance (10 points). In addition to reaching the pass mark for the attributes you must demonstrate that you have the level of English prescribed in the Immigration Rules and produce evidence of the required funds.<br>
