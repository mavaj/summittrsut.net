            <h4>UK Investor Category</h4>   


            <p><strong>Tier 1 (UK Investor Category)</strong><br>
                <br>
                <strong>Important Notice:</strong><br>
                Foreign entrepreneurs and investors are being given an extra incentive to come to the UK today by new visa rules, which will reward those who contribute to economic growth.<br>
                Under changes to the Immigration Rules, which were laid in Parliament today, people who come to the UK under Tier 1 (Investor) of the points-based system will be able to settle here faster if they invest large sums of money. Those who invest £5 million will be allowed to settle here after 3 years, and those investing £10 million or more will be allowed to settle after 2 years. This compares with the current minimum 5-year requirement.<br>
                The right to accelerated settlement will apply to those investors who are already in the UK and have invested large sums, as well as new arrivals.<br>
                Tier 1 (Entrepreneur) migrants will also be able to settle in the UK more quickly if they create 10 jobs or turn over £5 million in a 3-year period.<br>
                Tier 1 (General) Migrant of the Points Based System was launched for foreign nationals living in the UK on 29 February 2008. The four sub-tiers are Tier 1 (General) Migrant, Tier 1 (Investor), Tier 1 (Entrepreneur) and Tier 1 (Post-Study Work).<br>
                <br>
                <strong>The UK</strong><br>
                Immigration to Britain and acquisition of the British citizenship is a complex legal process that requires involvement of professional advisers.<br>
                <br>
                Our advisers will provide you with comprehensive correct up-to-date information on immigration law immigration programs available and will assist you every step of your chosen path to the residence in the UK and British citizenship. We provide full scope of immigration advice and services in the UK in relation to all immigration categories, Tier 1 of Points Based System (including Investors and Entrepreneurs),<br>
                <br>
                The &ldquo;UK Immigrant Investor Program&rdquo; was created by the UK Government in 1994 to attract high net worth individuals and their families to immigrate by making a substantial investment in the UK. For many high net worth individuals, the &ldquo;Investor&rdquo; category is the option they decide to pursue.<br>
                <br>
                <strong>Why Tier 1 (Investor) visa?</strong><br>
                <strong>No initial requirements</strong> as to the Investor&rsquo;s <strong>education, age, language skills or business experience;</strong> <strong>No requirements</strong> as to how many <strong>full time jobs the Investor would need to create.</strong> Investor does not need to hire any workers in the UK, if he/she invests money in one of the &lsquo;specified&rsquo; ways; Investor should be able to <strong>invest a minimum of £ 1,000,000.00 into the UK economy.</strong> <strong>No investment will be required until the application is approved and the applicant arrived to the UK;</strong><br>
                Money could be invested in the UK already – up to 12 months prior the application date – or could be invested in the UK within 3 months from investor&rsquo;s arrival here/grant for leave to remain to the applicant who is in the UK already and is permitted to switch into Tier 1; Permitted forms of investment are: Investment into the <strong>United Kingdom Government bonds</strong>, or Investment into the <strong>share capital or loan capital in an active and trading United Kingdom registered company/-ies</strong>, other than those principally engaged in property investment. Investor&rsquo;s family, including spouse and children under age 18, are all permitted to enter the UK and to apply for leave to remain and settlement here in due course. Family can join investor after his arrival to the UK or could apply for leave to enter simultaneously with the main applicant;<br>
                Investor&rsquo;s <strong>spouse will have the full right to engage in business activities or employment</strong> in the UK on the ground of her &lsquo;dependent&rsquo;s visa, without the need of any specific approval or permission; Investor may travel outside of the UK at any time, within term of his/her visa and may reside anywhere within the UK;<br>
                Investors <strong>may spend up to 180 days in one calendar year outside the UK;</strong> After the Investor has spent legally in the UK over 5 years and at least the last 12 months resided here as a settled person (permanent resident), he/she will have the right to apply<strong>for naturalization as a British Citizen</strong> and apply to register minor children as British Citizens too, provided that he/she is able to meet other requirements, including &lsquo;Good Character&rsquo; requirement, and is free from criminal convictions in any country.<br>
                <br>
                <strong>Main Requirements</strong><br>
                The applicant will need to score a minimum of 75 points under the Points Based System by satisfying one of the following criteria:<br>
                The Investor has access to at least £ 1,000,000 (held in GB pounds or other currency) of his own money, under his control and the money is held in a &rsquo;regulated financial institution&rsquo; (that is, a financial institution regulated by Home Regulator) and is disposable in the United Kingdom. The money can belong to the Investor or to his spouse/partner however if the money are held in spouse&rsquo;s /partner&rsquo;s name then the applicant will need to demonstrate that the money is available to him/her and is, effectively, under his/her control.<br>
                Or<br>
                The Investor owns personal assets which have a value exceeding £2,000,000, taking into account any liabilities to which you are subject, and have money under your control in the United Kingdom amounting to no less than £1,000,000. This may include money loaned to you provided that it is loaned by a financial institution regulated by the UK Financial Services Authority. Capital Immigration is able to help you arrange these loans which are secured on your UK investments.<br>
                The remaining £250,000 may be invested as you wish.<br>
                <br>
            </p>
            <div><img src="http://www.capitalimmigration.net/Images/uk_031.jpg" alt="europe_002"></div>
            <strong>Financed Option Available</strong><br>
            We are able to help arrange a financing service through a Major UK-based Investment Bank to help you meet the investment requirements of the UK Government&rsquo;s Investor Program.<br>
            For example, you would remit approximately £250,000 to the bank. Upon approval of your initial immigration application by the UK Home Office, they would grant a loan which you would then use to make your investment. This would fulfill the government requirement of £1,000,000 invested in the UK investor program. To help safeguard the investment and the loan, the investment bank will offer a range of eligible investments you can make.<br>
            <br>
            <strong>The remaining criteria are:</strong><br>
            Being able to maintain and accommodate yourself and any dependants without taking employment (other than self employment or business)<br>
            You may not take employment beyond managing your investments.<br>
            You must make the UK your home, you do not need to spend all your time in the UK, but you should spend at least 50% of your time here.<br>
            <br>
            <strong>The application process</strong><br>
            You initial visa will be valid for a period of 12 months. It will be based on your demonstrated assets and your stated plans. No investment is required before visa issuance.<br>
            Within 3 months of your arrival in the UK you should have transferred the funds to the UK.<br>
            At the end of your first 12 months in the UK, you may apply for your visa to be extended for a further 3 years. After a total of 4 years in the UK you may apply for indefinite leave to remain (Permanent Residence). A year after being granted indefinite leave to remain you may apply for naturalization as a British Citizen.<br>
