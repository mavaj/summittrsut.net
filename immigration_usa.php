            <h4>Non-Immigrant Category - USA E2 Visa</h4>   

            <p><b>Overview:</b>
                E2 Investor Visa for investors who do not have the $500,000 investment capital for U.S. Legal Permanent Residency or green card. An E-2 visa requires that the investor purchase a U.S. Business (pre existing or they start their own) for a minimum of approximately $200,000.00. The investor must work in the business-they will not get work authorization. However, their spouse will get work authorization. The investor will come on a 2 year visa as will the investor's spouse and children under 21. </p>
            <p>After 2 years, the investor must renew the visa showing that the business is still in place. Renewals are unlimited. This visa does not turn into a green card.</p>
            <p>The E2 Investor Visa works well for younger investors who wish to live in the U.S. or for couples where 1 spouse has a degree and can get a job and then petition the employer to get the family green cards. Gives the family an opportunity to come to the United States, enroll their children in school, run a business and then the spouse with the degree can go obtain employment.
            </p>
            <p>The time period is very quick. Processing time is 3-4 months. </p>
            <p>The Treaty Investor (E-2) visa is for an applicant whose purpose for entering the U.S. is solely to develop and direct the operations of an enterprise in which the alien has invested, or is actively in the process of investing, a substantial amount of capital in a bona fide enterprise.</p>
            <p>To determine whether the investment is “substantial” the Department of State employs a relative- proportionality test.</p>
            <p>Applicants are able to place funds in escrow pending the approval of the E classification provided that there is a legal mechanism that irrevocably commits funds if the application is approved.
            </p>

            <h6>Requirements:</h6>
            <p>Treaty investor applicants must meet specific requirements to qualify for a treaty investor (E-2) visa. The consular officer will determine whether a treaty investor applicant qualifies for a visa.</p>
            <ul>
                <li><p>The investor, either a real or corporate person, must be a national of a treaty country.</li>
                <li><p>The investment must be substantial. It must be sufficient to ensure the successful operation of the enterprise. The percentage of investment for a low-cost business enterprise must be higher than the percentage of investment in a high-cost enterprise.</p></li>
                <li><p>The investment must be a real operating enterprise. Speculative or idle investment does not qualify. Uncommitted funds in a bank account or similar security are not considered an investment.</p></li>
                <li><p>The investment may not be marginal. It must generate significantly more income than just to provide a living to the investor and family, or it must have a significant economic impact in the U.S.</p></li>
                <li><p>The investor must have control of the funds, and the investment must be at risk in the commercial sense. Loans secured with the assets of the investment enterprise are not allowed.</p></li>
                <li><p>The investor must be coming to the U.S. to develop and direct the enterprise. If the applicant is not the principal investor, he or she must be employed in a supervisory, executive, or highly specialized skill capacity.</p></li>
