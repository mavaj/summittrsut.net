          <p><strong>Office space requirements</strong></p>
          <p>An entity set-up in the UAE is always a combination of license, legal entity and facility. The UAE has always been a real estate-driven economy and it is law that a company license is always linked to the size of the office space as stated in the official office space tenancy contract.</p>
          <p>Several free zones offer flexible desk solutions to provide companies with affordable options for office space. The conditions of minimum office space differ from one jurisdiction to another. Local statutory rules will often require companies to appoint officers such as a manager, directors and a company secretary.</p>
